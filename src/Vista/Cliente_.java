    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String codigo = txtCodBus.getText();
        Cliente bus = cad_cliente.buscar(codigo);
        if (bus == null) {
            JOptionPane.showMessageDialog(null, "El codigo del cliente es invalido");
        } else {
            DefaultTableModel tb = (DefaultTableModel) jTable1.getModel();
            tb.setRowCount(0);
            Object v[] = {bus.getCodigo_cliente(), bus.getNombre(), bus.getApellido(), bus.getDni(), bus.getEdad(), bus.getMonto_compras_realizadas()};
            tb.addRow(v);
            Premio p = new Premio();
            txtPremio.setText(""+p.premio(bus.getMonto_compras_realizadas()));
        }
    }//GEN-LAST:event_jButton1ActionPerformed

