    void muestra() {
        DefaultTableModel dt = (DefaultTableModel) tablaVentas.getModel();
        dt.setRowCount(0);
        for (Ventas_general p : cad_ventas_generales.obtener()) {
            Object v[] = {p.getCodigo_venta() ,p.getCodigo_p(), p.getCompra(), p.getDescuento(), p.getTotal()};
            dt.addRow(v);
        }
    }
